/* 2 floats class */
public class vec2
{
    public float X, Y;

    /* vec2 class constructor
     * ARGUMENTS:
     *   - New coords:
     *       float NewX, NewY;
     */
    public vec2( float NewX, float NewY )
    {
        X = NewX;
        Y = NewY;
    } /* End of 'vec2' function */

    /* vec2 class constructor
     * ARGUMENTS: None.
     */
    public vec2()
    {
        X = 0;
        Y = 0;
    } /* End of 'vec2' function */
} /* End of 'vec2' class */
